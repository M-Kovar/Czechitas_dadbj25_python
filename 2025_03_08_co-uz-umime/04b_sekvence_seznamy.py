# Sekvence: Indexování

guest_list = ["Jirka", "Klára", "Natálie"]
print(guest_list)

# Indexovani sekvenci zacina od nuly
guest_list = ["Jirka", "Klára", "Natálie"]
# POZICE:       1.        2.        3.
# INDEX:        0         1         2

print(guest_list[0])    # Prvni polozka seznamu (index 0)
print(guest_list[1])    # Druha polozka seznamu (index 1)
print(guest_list[2])    # Treti polozka seznamu (index 2)

print(guest_list[-1])   # Posledni polozka seznamu
print(guest_list[-2])   # Predposledni polozka seznamu